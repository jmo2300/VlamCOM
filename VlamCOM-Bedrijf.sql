-- Tabel Afdelingen
DROP TABLE IF EXISTS Afdelingen;
CREATE TABLE Afdelingen (
    AfdelingID INT AUTO_INCREMENT PRIMARY KEY,
    AfdelingNaam VARCHAR(50) NOT NULL
);

-- Tabel Medewerkers
DROP TABLE IF EXISTS Medewerkers;
