    MedewerkerID INT AUTO_INCREMENT PRIMARY KEY,
    Voornaam VARCHAR(50),
    Achternaam VARCHAR(50),
    Functie VARCHAR(50),
    AfdelingID INT,
    Locatie VARCHAR(50),
    Salaris DECIMAL(10, 2),
    FOREIGN KEY (AfdelingID) REFERENCES Afdelingen(AfdelingID)
);

